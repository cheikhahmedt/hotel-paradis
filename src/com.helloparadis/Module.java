class Module{

    // Propriete de la classe Module
    private String nom;
    private int coef;

    //Conctructeur
    public Module(String nom, int coef) {
    }


    // Setters and Getters
    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getCoef() {
        return coef;
    }

    public void setCoef(int coef) {
        this.coef = coef;
    }
}